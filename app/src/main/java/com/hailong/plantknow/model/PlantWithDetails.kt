package com.hailong.plantknow.model

/**
 * 完整的植物信息数据实体
 * 融合了百度AI的精准识别结果和阿里云大模型的详细描述
 * 实现"精准识别 + 丰富解读"的双AI引擎优势互补
 */
data class PlantWithDetails(
    /**
     * 百度植物识别API返回的基础植物信息
     * 包含植物名称、识别置信度、基础描述等核心数据
     * 特点：识别准确率高（97%+），响应速度快
     *
     * @see PlantResult 百度AI识别结果数据模型
     */
    val basicInfo: PlantResult,

    /**
     * 阿里云通义千问生成的详细植物描述
     * 包含植物特征、生长习性、用途价值等扩展信息
     * 特点：内容丰富生动，具有教育性和可读性
     *
     * 示例内容：
     * "玫瑰是蔷薇科蔷薇属的落叶灌木，原产于中国。花朵通常为红色、粉色、白色或黄色，
     * 花瓣层层叠叠，香气浓郁。玫瑰喜欢阳光充足、排水良好的环境，花期主要在5-9月。
     * 除了观赏价值，玫瑰还可用于提炼精油、制作花茶和化妆品原料。"
     */
    val detailedDescription: String
)